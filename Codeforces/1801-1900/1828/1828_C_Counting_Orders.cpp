const int MAXN = 200010;
int N;
map<int, int> master;
map<int, int> a, b;

ll fact[1000010], factinv[1000010];

ll choose(int a, int b)
{
    return mult(fact[a], mult(factinv[b], factinv[a - b]));
}

void get_fact(int x)
{
    fact[0] = 1;
    for (int i = 1; i <= x; i++) fact[i] = mult(i, fact[i - 1]);
    factinv[x] = divide(1, fact[x]);
    for (int i = x - 1; i >= 0; i--) factinv[i] = mult(factinv[i + 1], i + 1);
}

void solve() {
    cin >> N;
    for (int i = 1; i <= N; i++) {
        int n;
        cin >> n;
        a[n * -1]++;
        master[n * -1] = 1;
    }
    for (int i = 1; i <= N; i++) {
        int n;
        cin >> n;
        b[n * -1]++;
        master[n * -1] = 1;
    }

    ll ans = 1;
    int seen = 0;
    int ac = 0;
    for (pii n : master) {
        if (b.count(n.FF)) {
            if (b[n.FF] > ac) ans = 0;
            else {
                multeq(ans, mult(fact[ac], factinv[ac - b[n.FF]]));
                ac -= b[n.FF];
                seen += b[n.FF];
            }
        }
        if (a.count(n.FF)) ac += a[n.FF];
    }

    cout << ans << endl;

    master.clear();
    a.clear();
    b.clear();
}

